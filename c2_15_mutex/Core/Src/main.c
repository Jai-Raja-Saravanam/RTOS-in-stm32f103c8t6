void delay(void){
	int i=0,j=0;
	for(i=0;i<1000;i++){
		for(j=0;j<10;j++);
	}
}

void inttostr(){
	unsigned int num = count;
	while(num){
		stringCount[0]=num%10+0x30;
		stringCount[1]=(num/10)%10+0x30;
		stringCount[2]=(num/100)%10+0x30;
		stringCount[3]=(num/1000)%10+0x30;
		stringCount[4]=(num/10000)%10+0x30;
	}
}
void periodicled_handler(void *pvparameter)
{

	while(1){

		xSemaphoreTake(mutex_1,portMAX_DELAY);

		count++;
		HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_0);
		HAL_UART_Transmit(&huart1, task1, 6, 100);
		HAL_UART_Transmit(&huart1, stringCount, 7, 100);

		xSemaphoreGive(mutex_1);


	}
}


void button_handler(void *pvparameter)
{
	uint8_t *data =(uint8_t*) pvparameter ;
	while(1){
		while(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_3)==1){
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, 0);
			xSemaphoreTake(mutex_1,portMAX_DELAY);
			count++;
			inttostr();
			HAL_UART_Transmit(&huart1, task2, 6, 100);
			HAL_UART_Transmit(&huart1, stringCount, 7, 100);
			delay();
		}
		xSemaphoreGive(mutex_1);
		HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, 1);
		vTaskDelay(pdMS_TO_TICKS(200));
		//xSemaphoreGive(s_work);
	}

}

